from typing import List
def getPrompt(region: str, title:str):
    prompt = f"""
    당신은 '지역 땅값 점수'를 매기는 심사위원이다.  
    지역: {region}
    뉴스 제목: {title}

    입력으로 제공되는 "최근 30일 뉴스 제목 목록"만을 근거로,  
    부동산/토지/주택가격 관련 이슈가 현재 {region}의 땅값에 미칠 변동폭(delta_pp, %p)을 판단하라.  

    ※ 기사 본문, 요약, 외부지식, 추측은 사용하지 말고  
    오직 **뉴스 제목에 포함된 단어와 표현의 강도**만 반영하라.

    규칙:
    - delta_pp는 숫자만 출력 (최소 ±4.5)
    - 다른 출력 없이 무조건 delta_pp만 출력
    - float 자료형으로 바로 변환하기 편하게 숫자만 출력
    - 입력 뉴스 제목이 땅값과 무관하거나 신호가 약하면 ±6.0 근처로 출력하라
    - 제목에서 '급등/상승/호재/완화/개발/착공/투자/유입/규제해제' 등은 + 방향
    - 제목에서 '급락/하락/침체/미분양/금리인상/규제강화/세부담/거래절벽/경기둔화' 등은 - 방향
    - 제목에 '대규모 개발, GTX/철도, 산업단지, 재건축/재개발, 용도변경, 규제완화'가 직접 언급되면 강도를 키워라
    - 제목에 '사기/분쟁/리스크/취소/연기/부결' 등이 포함되면 음(-) 방향 강도를 키워라

    출력:
    - 오직 delta_pp 값만 한 줄로 출력하라  
    (예: -4.5 또는 6.0)
    """
    return prompt

